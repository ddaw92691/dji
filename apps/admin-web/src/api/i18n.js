import request from '@/utils/request';
export const i18nApi = {
    getCountries: (params) => request.get('/admin/i18n/countries', { params }),
    createCountry: (data) => request.post('/admin/i18n/countries', data),
    updateCountry: (id, data) => request.put(`/admin/i18n/countries/${id}`, data),
    updateCountryStatus: (id, status) => request.put(`/admin/i18n/countries/${id}/status`, { status }),
    deleteCountry: (id) => request.delete(`/admin/i18n/countries/${id}`),
    getLanguages: (params) => request.get('/admin/i18n/languages', { params }),
    createLanguage: (data) => request.post('/admin/i18n/languages', data),
    updateLanguage: (id, data) => request.put(`/admin/i18n/languages/${id}`, data),
    updateLanguageStatus: (id, status) => request.put(`/admin/i18n/languages/${id}/status`, { status }),
    deleteLanguage: (id) => request.delete(`/admin/i18n/languages/${id}`),
    getCountryLanguages: (params) => request.get('/admin/i18n/country-languages', { params }),
    bindCountryLanguage: (data) => request.post('/admin/i18n/country-languages', data),
    setDefaultCountryLanguage: (id) => request.put(`/admin/i18n/country-languages/${id}/default`),
    deleteCountryLanguage: (id) => request.delete(`/admin/i18n/country-languages/${id}`),
    getNamespaces: (params) => request.get('/admin/i18n/namespaces', { params }),
    createNamespace: (data) => request.post('/admin/i18n/namespaces', data),
    updateNamespace: (id, data) => request.put(`/admin/i18n/namespaces/${id}`, data),
    updateNamespaceStatus: (id, status) => request.put(`/admin/i18n/namespaces/${id}/status`, { status }),
    deleteNamespace: (id) => request.delete(`/admin/i18n/namespaces/${id}`),
    getTranslations: (params) => request.get('/admin/i18n/translations', { params }),
    getGroupedTranslations: (params) => request.get('/admin/i18n/translations/grouped', { params }),
    batchSaveTranslations: (entries) => request.post('/admin/i18n/translations/batch', { entries }),
    createTranslation: (data) => request.post('/admin/i18n/translations', data),
    updateTranslation: (id, data) => request.put(`/admin/i18n/translations/${id}`, data),
    updateTranslationStatus: (id, status) => request.put(`/admin/i18n/translations/${id}/status`, { status }),
    deleteTranslation: (id) => request.delete(`/admin/i18n/translations/${id}`),
    importTranslations: (data) => request.post('/admin/i18n/translations/import', data),
    exportTranslations: (params) => request.get('/admin/i18n/translations/export', { params }),
    autoTranslate: (data) => request.post('/admin/i18n/translations/auto-translate', data, { timeout: 600000 }),
};
